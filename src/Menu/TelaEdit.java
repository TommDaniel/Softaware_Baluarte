    private void iniciarCategorias(){
        
        File file = new File("save/dadosCategorias.ser");

        // Verifica se o arquivo existe antes de tentar lê-lo
        if (!file.exists()) {
            String[] c = {"Camiseta", "Boné", "Caneca", "Xícara"};

            for (int i = 0; i < c.length; i++) {
                Produtos.addCategoria(c[i]);
                adicionarItemSeNaoExistir(c[i]);
            }
            combobox2.setSelectedIndex(-1);
            return;
        }
        try (FileInputStream fileIn = new FileInputStream("save/dadosCategorias.ser"); ObjectInputStream objectIn = new ObjectInputStream(fileIn)) {
            ArrayList<String> nCategorias = (ArrayList<String>) objectIn.readObject();
            Produtos.setCategorias(nCategorias);
            
            for(String cat : Produtos.getCategorias()){
                
                adicionarItemSeNaoExistir(cat);
            }
            combobox2.setSelectedIndex(-1);
        } catch (IOException | ClassNotFoundException e) {
            e.printStackTrace();
        }
        
        
    }
    public void adicionarItemSeNaoExistir(String novoItem) {
        // Converte o novo item para minúsculas para a comparação ignorar maiúsculas e minúsculas
        String novoItemMinusc = novoItem.toLowerCase();

        boolean itemExiste = false;
        for (int i = 0; i < combobox2.getItemCount(); i++) {
            String itemExistente;
            itemExistente = (String) combobox2.getItemAt(i);
            itemExistente = itemExistente.toLowerCase();
            if (itemExistente.equals(novoItemMinusc)) {
                itemExiste = true;
                break;
            }
        }

        if (!itemExiste) {
            combobox2.addItem(novoItem);
        }
    }
    
    
