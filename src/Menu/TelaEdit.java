    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String textoPreco = textField10.getText();
            textoPreco = textoPreco.replace(',', '.');
            int quantidade = Integer.parseInt(textField9.getText());
            double preco = Double.parseDouble(textoPreco);
            
            Object[] produtosT = {idSelecionado,combobox2.getSelectedItem(),textField8.getText(),quantidade,textField10.getText(),color};
            TelaProdutos.getInstancia().editaTabela(produtosT,(idSelecionado));
            Produtos.removeProduto(idSelecionado);
            Produtos.addProduto(new Produto(idSelecionado,(String) combobox2.getSelectedItem(),textField8.getText(),quantidade,preco,color));
            telaEdit.dispose();
        }catch (NumberFormatException e) {
            // Trate o erro, como mostrar uma mensagem de erro ao usuário
            System.out.println("Entrada inválida, por favor insira um número inteiro.");
            JOptionPane.showMessageDialog(null, "Numero Invalido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void combobox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combobox1ActionPerformed
        // TODO add your handling code here:
        idSelecionado = (Integer) combobox1.getSelectedItem();
        if(idSelecionado!=null){
            
            textField8.setEditable(true);
            textField9.setEditable(true);
            textField10.setEditable(true);
            textField8.setText(Produtos.getProduto(idSelecionado).getDescricao());
            textField9.setText(""+Produtos.getProduto(idSelecionado).getQuantidade());
            textField10.setText(""+ Produtos.getProduto(idSelecionado).getPreco());
            color = Produtos.getProduto(idSelecionado).getCor();

            if(color == null){
                color = color.WHITE;
            }
            jPanel3.setBackground(color);
            
            for (int i = 0; i < combobox2.getItemCount(); i++) {
                if(combobox2.getItemAt(i).equals(Produtos.getProduto(idSelecionado).getCategoria())){
                    combobox2.setSelectedIndex(i);
                }
            }
        }
    }//GEN-LAST:event_combobox1ActionPerformed


