    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        int row = tableDark1.getSelectedRow();
        if (row >= 0) {
            
            String apenasNumeros = jTextField1.getText().replaceAll("[^\\d.]", "");
            double valorTotal = Double.parseDouble(apenasNumeros);
            
            double valorCelula =(double) tableDark1.getModel().getValueAt(row, 4);
            int quantidadeCelula =(int) tableDark1.getModel().getValueAt(row, 3);
            valorTotal -= (valorCelula*quantidadeCelula);
            formato.setRoundingMode(java.math.RoundingMode.HALF_UP);
            modelo.removeRow(row);
            jTextField1.setText("R$ " + formato.format(valorTotal));
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void textField9FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textField9FocusLost
        // TODO add your handling code here:
        String textoPreco = textField9.getText().trim();
        if (!textoPreco.isEmpty()) {
            try {
                double valorPago = Double.parseDouble(textoPreco.replace(',', '.'));
                String apenasNumeros = jTextField1.getText().replaceAll("[^\\d.]", "");
                double valorTotal = Double.parseDouble(apenasNumeros);
                double faltante = valorTotal-valorPago;
                jTextField4.setText(""+formato.format(faltante));
                
            }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido.", "Valor Inválido", JOptionPane.ERROR_MESSAGE);
            }
        }else {
            JOptionPane.showMessageDialog(this, "O campo de valor recebido está vazio.", "Valor Vazio", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_textField9FocusLost

