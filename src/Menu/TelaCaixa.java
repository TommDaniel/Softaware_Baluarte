    public void adicionarItemSeNaoExistir(int novoItem) {
        String Item = novoItem + " " + Produtos.getProduto(novoItem).getDescricao();
        String novoItemMinusc = Item.toLowerCase();
        boolean itemExiste = false;
        for (int i = 0; i < combobox2.getItemCount(); i++) {
            
            String itemExistente = (String) combobox2.getItemAt(i);
            if (itemExistente.toLowerCase().equals(novoItemMinusc)) {
                itemExiste = true;
                break;
            }
        }

        if (!itemExiste) {
            combobox2.addItem(novoItem + " " + Produtos.getProduto(novoItem).getDescricao());
        }
    }
    
